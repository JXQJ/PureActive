using System;
using PureActive.Network.Abstractions.Types;

namespace PureActive.Network.Abstractions.DeviceInfo
{
    /// <summary>
    /// Interface IDeviceInfo
    /// </summary>
    /// <autogeneratedoc />
    public interface IDeviceInfo
    {
        /// <summary>
        /// Gets the identifier.
        /// </summary>
        /// <value>The identifier.</value>
        /// <autogeneratedoc />
        string Id { get; }

        /// <summary>
        /// Gets the model.
        /// </summary>
        /// <value>The model.</value>
        /// <autogeneratedoc />
        string Model { get; }

        /// <summary>
        /// Gets the manufacturer.
        /// </summary>
        /// <value>The manufacturer.</value>
        /// <autogeneratedoc />
        string Manufacturer { get; }

        /// <summary>
        /// Gets the name of the device.
        /// </summary>
        /// <value>The name of the device.</value>
        /// <autogeneratedoc />
        string DeviceName { get; }

        /// <summary>
        /// Gets the version.
        /// </summary>
        /// <value>The version.</value>
        /// <autogeneratedoc />
        string Version { get; }

        /// <summary>
        /// Gets the version number.
        /// </summary>
        /// <value>The version number.</value>
        /// <autogeneratedoc />
        Version VersionNumber { get; }

        /// <summary>
        /// Gets the application version.
        /// </summary>
        /// <value>The application version.</value>
        /// <autogeneratedoc />
        string AppVersion { get; }

        /// <summary>
        /// Gets the application build.
        /// </summary>
        /// <value>The application build.</value>
        /// <autogeneratedoc />
        string AppBuild { get; }

        /// <summary>
        /// Gets the device platform.
        /// </summary>
        /// <value>The device platform.</value>
        /// <autogeneratedoc />
        DevicePlatform DevicePlatform { get; }

        /// <summary>
        /// Gets the type of the device.
        /// </summary>
        /// <value>The type of the device.</value>
        /// <autogeneratedoc />
        DeviceType DeviceType { get; }

        /// <summary>
        /// Gets a value indicating whether this instance is device.
        /// </summary>
        /// <value><c>true</c> if this instance is device; otherwise, <c>false</c>.</value>
        /// <autogeneratedoc />
        bool IsDevice { get; }
    }
}