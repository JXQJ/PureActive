using System;
using System.Collections.Generic;
using System.Runtime.CompilerServices;
using System.Threading;
using System.Threading.Tasks;
using Microsoft.Extensions.Logging;
using PureActive.Logging.Abstractions.Interfaces;

namespace PureActive.Hosting.Abstractions.Extensions
{
    /// <summary>
    /// Class TaskExtensions.
    /// </summary>
    /// <autogeneratedoc />
    public static class TaskExtensions
    {
        /// <summary>
        /// Waits for tasks.
        /// </summary>
        /// <param name="tasks">The tasks.</param>
        /// <param name="cancellationToken">The cancellation token.</param>
        /// <param name="onTaskComplete">The on task complete.</param>
        /// <param name="logger">The logger.</param>
        /// <param name="memberName">Name of the member.</param>
        /// <returns>Task.</returns>
        /// <autogeneratedoc />
        public static Task WaitForTasksAction(this List<Task> tasks, CancellationToken cancellationToken, Action<Task> onTaskComplete,
            IPureLogger logger = null, [CallerMemberName] string memberName = "")
        {
            Task t = Task.WhenAll(tasks);

            try
            {
                t.Wait(cancellationToken);

                onTaskComplete(t);
            }
            catch (OperationCanceledException ex)
            {
                logger?.LogError("Task {Method} timed out", memberName);

                t = Task.FromException(ex);
            }
            catch (Exception ex)
            {
                logger?.LogError(ex, "Task {Method} threw an exception", memberName);

                t = Task.FromException(ex);
            }

            return t;
        }
    }
}