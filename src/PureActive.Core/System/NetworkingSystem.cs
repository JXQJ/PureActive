using System;
using System.Collections.Generic;
using System.Net.NetworkInformation;
using System.Text;
using PureActive.Core.Abstractions.System;
using PureActive.Logging.Abstractions.Interfaces;

namespace PureActive.Core.System
{
    /// <summary>
    /// Class NetworkingSystem.
    /// Implements the <see cref="INetworkingSystem" />
    /// </summary>
    /// <seealso cref="INetworkingSystem" />
    /// <autogeneratedoc />
    public class NetworkingSystem : INetworkingSystem
    {
        private readonly IPureLogger _logger;

        /// <summary>
        /// Constructor for NetworkingSystem
        /// </summary>
        /// <param name="logger"></param>
        public NetworkingSystem(IPureLogger<NetworkingSystem> logger)
        {
            _logger = logger ?? throw new ArgumentNullException(nameof(logger));
        }

        /// <summary>
        /// Gets all network interfaces.
        /// </summary>
        /// <returns>NetworkInterface[].</returns>
        /// <autogeneratedoc />
        public NetworkInterface[] GetAllNetworkInterfaces()
        {
            return NetworkInterface.GetAllNetworkInterfaces();
        }
    }
}
