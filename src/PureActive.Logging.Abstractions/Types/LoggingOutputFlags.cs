using System;

namespace PureActive.Logging.Abstractions.Types
{
    /// <summary>
    /// Enum LoggingOutputFlags
    /// </summary>
    /// <autogeneratedoc />
    [Flags]
    public enum LoggingOutputFlags
    {
        /// <summary>
        /// The default
        /// </summary>
        /// <autogeneratedoc />
        Default = 0,
        /// <summary>
        /// The console
        /// </summary>
        /// <autogeneratedoc />
        Console = 1 << 0,
        /// <summary>
        /// The rolling file
        /// </summary>
        /// <autogeneratedoc />
        RollingFile = 1 << 1,
        /// <summary>
        /// The application insights
        /// </summary>
        /// <autogeneratedoc />
        AppInsights = 1 << 2,
        /// <summary>
        /// The x unit console
        /// </summary>
        /// <autogeneratedoc />
        XUnitConsole = 1 << 3,
        /// <summary>
        /// The test correlator
        /// </summary>
        /// <autogeneratedoc />
        TestCorrelator = 1 << 4,

        /// <summary>
        /// The testing
        /// </summary>
        /// <autogeneratedoc />
        Testing = XUnitConsole | TestCorrelator,
        /// <summary>
        /// The application console file
        /// </summary>
        /// <autogeneratedoc />
        AppConsoleFile = Console | RollingFile,
        /// <summary>
        /// The application full
        /// </summary>
        /// <autogeneratedoc />
        AppFull = AppConsoleFile | AppInsights,
        /// <summary>
        /// The testing application console file
        /// </summary>
        /// <autogeneratedoc />
        TestingAppConsoleFile = Console | RollingFile | Testing
    }
}