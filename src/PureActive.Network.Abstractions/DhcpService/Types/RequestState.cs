namespace PureActive.Network.Abstractions.DhcpService.Types
{
    /// <summary>
    /// Enum RequestState
    /// </summary>
    /// <autogeneratedoc />
    public enum RequestState
    {
        /// <summary>
        /// The unknown
        /// </summary>
        /// <autogeneratedoc />
        Unknown = 0,
        /// <summary>
        /// The selecting
        /// </summary>
        /// <autogeneratedoc />
        Selecting,
        /// <summary>
        /// The renewing
        /// </summary>
        /// <autogeneratedoc />
        Renewing,
        /// <summary>
        /// The rebinding
        /// </summary>
        /// <autogeneratedoc />
        Rebinding,
        /// <summary>
        /// The initialize reboot
        /// </summary>
        /// <autogeneratedoc />
        InitReboot,
        /// <summary>
        /// The duplicate
        /// </summary>
        /// <autogeneratedoc />
        Duplicate
    }

    /// <summary>
    /// Class RequestStateString.
    /// </summary>
    /// <autogeneratedoc />
    public static class RequestStateString
    {
        /// <summary>
        /// Gets the name.
        /// </summary>
        /// <param name="requestStateEnum">The request state enum.</param>
        /// <returns>System.String.</returns>
        /// <autogeneratedoc />
        public static string GetName(RequestState requestStateEnum)
        {
            switch (requestStateEnum)
            {
                case RequestState.Selecting:
                    return "SELECTING";
                case RequestState.Renewing:
                    return "RENEWING";
                case RequestState.Rebinding:
                    return "REBINDING";
                case RequestState.InitReboot:
                    return "INIT-REBOOT";
                case RequestState.Duplicate:
                    return "DUPLICATE";
                default:
                    return "UNKNOWN";
            }
        }
    }
}