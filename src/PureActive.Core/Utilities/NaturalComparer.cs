using System;
using System.Collections.Generic;
using System.Text.RegularExpressions;

namespace PureActive.Core.Utilities
{
    /// <summary>
    /// Taken from http://www.codeproject.com/Articles/22517/Natural-Sort-Comparer
    /// Implements the <see cref="IDisposable" />
    /// </summary>
    /// <seealso cref="IDisposable" />
    public class NaturalComparer : Comparer<string>, IDisposable
    {
        /// <summary>
        /// The table
        /// </summary>
        /// <autogeneratedoc />
        private Dictionary<string, string[]> _table;

        /// <summary>
        /// Initializes a new instance of the <see cref="NaturalComparer"/> class.
        /// </summary>
        /// <autogeneratedoc />
        public NaturalComparer()
        {
            _table = new Dictionary<string, string[]>();
        }

        /// <summary>
        /// Performs application-defined tasks associated with freeing, releasing, or resetting unmanaged resources.
        /// </summary>
        /// <autogeneratedoc />
        public void Dispose()
        {
            _table.Clear();
            _table = null;
        }

        /// <summary>
        /// Compares the specified x.
        /// </summary>
        /// <param name="x">The x.</param>
        /// <param name="y">The y.</param>
        /// <returns>System.Int32.</returns>
        /// <autogeneratedoc />
        public override int Compare(string x, string y)
        {
            if (x == y || string.IsNullOrEmpty(x) || string.IsNullOrEmpty(y)) return 0;

            if (!_table.TryGetValue(x, out var x1))
            {
                x1 = Regex.Split(x.Replace(" ", ""), "([0-9]+)");
                _table.Add(x, x1);
            }

            if (!_table.TryGetValue(y, out var y1))
            {
                y1 = Regex.Split(y.Replace(" ", ""), "([0-9]+)");
                _table.Add(y, y1);
            }

            for (var i = 0; i < x1.Length && i < y1.Length; i++)
                if (x1[i] != y1[i])
                    return PartCompare(x1[i], y1[i]);
            if (y1.Length > x1.Length)
                return 1;
            if (x1.Length > y1.Length)
                return -1;
            return 0;
        }

        /// <summary>
        /// Parts the compare.
        /// </summary>
        /// <param name="left">The left.</param>
        /// <param name="right">The right.</param>
        /// <returns>System.Int32.</returns>
        /// <autogeneratedoc />
        private static int PartCompare(string left, string right)
        {
            if (!int.TryParse(left, out var x)) return string.Compare(left, right, StringComparison.Ordinal);

            return !int.TryParse(right, out var y)
                ? string.Compare(left, right, StringComparison.Ordinal)
                : x.CompareTo(y);
        }
    }
}